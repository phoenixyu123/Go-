package main

import "fmt"

func main() {
	//[!]位运算也使用补码进行运算*
	//2&3:
	// 2补码[0000 0010]
	// 3补码[0000 0011]-->2&3 == [0000 0010] ==2
	fmt.Println(2 & 3)
	//2|1
	// 2[0000 0010]
	// 1[0000 0011] -->2|1 == [0000 0011] ==3
	fmt.Println(2 | 1)
	//2^3   2异或3
	// 2[0000 0010]
	// 3[0000 0011]
	// -->[0000 0001]==1
	fmt.Println(2 ^ 3)
	//-1^3
	// -1[1000 0001]--[1111 1110]--[1111 1111]
	// 3[0000 0011]
	// -1^3 =[1111 1100]--[1111 1011]--[1000 0100]==-4
	fmt.Println(-1 ^ 3)
	//-2^2
	// -2[1000 0010]--[1111 1101]--[1111 1110]
	// 2[0000 0010]
	// -2^2 =[1111 1100]--[1111 1011]--[1000 0100]
	fmt.Println(-2 ^ 2)
	
}
